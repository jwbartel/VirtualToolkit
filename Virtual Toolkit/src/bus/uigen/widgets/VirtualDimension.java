package bus.uigen.widgets;

public class VirtualDimension {
	int width, height;

	public VirtualDimension(int aWidth, int aHeight) {
		width = aWidth;
		height = aHeight;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

}
